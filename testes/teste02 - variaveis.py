#Guardando as Informações
#Variaveis -> Espaços na memória do programa para guardar um tipo de informação
#Exemplo: nome_da_variavel = valor
#Declarar Variavel -> Criar uma variavel com um valor inicial

nome = "Murilo Bonassi" #String
idade = 31 #Int
altura = 1.67 #Float
trabalha = True #Boolean
calculo = 10*3+(40/2) #Lógica -> Guarda o resultado

#Exibindo as informações
#1ª Forma -> Concatenando as Informações
print("Meu nome é",nome)
print("Eu tenho",idade,"anos")
#2ª Forma -> Formatando com as Informações
print(f"Eu tenho altura de {altura}m")
print(f"Minha situação de trabalho é {trabalha}")
print(f"10*3+(40/2) = {calculo}")