print("| Calculos |")
numero1 = float(input("Digite o 1º Número: "))
numero2 = float(input("Digite o 2º Número: "))

adi = numero1+numero2
sub = numero1-numero2
div = numero1/numero2
mul = numero1*numero2
pot = numero1**numero2

print(f"{numero1} + {numero2} = {adi}")
print(f"{numero1} - {numero2} = {sub}")
print(f"{numero1} / {numero2} = {div}")
print(f"{numero1} * {numero2} = {mul}")
print(f"{numero1} ** {numero2} = {pot}")